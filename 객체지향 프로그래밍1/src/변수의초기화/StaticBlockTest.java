package 변수의초기화;

public class StaticBlockTest {
	static int[] arr = new int[10];
	
	static {
		for(int i=0; i<arr.length; i++)
			arr[i] = (int)(Math.random()*10+1); // 1~10 중 임의의 값을 배열 arr에 저장한다.
	}
	
	public static void main(String[] args) {
		for(int i=0; i<arr.length; i++)
			System.out.println("arr["+i+"] : " + arr[i]);
	}
}
